// Экспорт для специалистов (аналогичная структура)
export const SPECIALIST_CATEGORIES: Record<string, CategoryConfig> = {
	...TASK_CATEGORIES, // Можно использовать те же категории
}

// Получить конфигурацию категории
export function getCategoryConfig(
	slug: string,
	type: 'tasks' | 'specialists' = 'tasks'
): CategoryConfig | undefined {
	const categories =
		type === 'tasks' ? TASK_CATEGORIES : SPECIALIST_CATEGORIES
	return categories[slug]
}

// Получить все категории
export function getAllCategories(
	type: 'tasks' | 'specialists' = 'tasks'
): CategoryConfig[] {
	const categories =
		type === 'tasks' ? TASK_CATEGORIES : SPECIALIST_CATEGORIES
	return Object.values(categories)
}

